private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
// TODO add your handling code here:
    String nomorMK = nomor_mk.getText();
    String namaMK = nama_mk.getText();
    
   if ((nomorMK.isEmpty()) | (namaMK.isEmpty()))
   {
       JOptionPane.showMessageDialog(null,"Data tidak boleh kosong, Silahkan dilengkapi");
       nomor_mk.requestFocus();
   }
   else
   {
       try
       {
           Class.forName(driver);
           Connection kon = DriverManager.getConnection(database,user,pass);
           Statement stt = kon.createStatement();
           String SQL = "UPDATE mata_kuliah "+ 
                   "SET kode_mk = '"+nomorMK+"',nama_mk = '"+namaMK+
                   "'WHERE "
                   + " kode_mk ='"+tableModel.getValueAt(row, 0).toString()+"';";
           stt.executeUpdate(SQL);
           data [0] = nomorMK;
           data [1] = namaMK;
           tableModel.removeRow(row);
           tableModel.insertRow(row, data);
           stt.close();
           kon.close();
           JOptionPane.showMessageDialog(this, "Data Berhasil Diubah");
           membersihkan_teks();
           simpan.setEnabled(false);
           nonaktif_text();
       }
       catch (Exception ex)
       {
         System.err.println(ex.getMessage());
       }
   }
}//GEN-LAST:event_ubahActionPerformed

