private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
// TODO add your handling code here:
    String data [] = new String[3];
    
    if ((nama_mk.getText().isEmpty()) || (nomor_mk.getText().isEmpty()))
    {
        JOptionPane.showMessageDialog(null,
                "Data tidak boleh kosong, silahkan dilengkapi");
        nomor_mk.requestFocus();
    }
    else
    {
        try
        {
            Class.forName(driver);
            Connection kon = DriverManager.getConnection(
                               database,
                               user,
                               pass);
            Statement stt = kon.createStatement();
            String    SQL = "INSERT INTO mata_kuliah(kode_mk,nama_mk) "
                    + "VALUES('"+nomor_mk.getText()+"','"+nama_mk.getText()+"')";
            stt.executeUpdate(SQL);
            data[0] = nomor_mk.getText();
            data[1] = nama_mk.getText();
            tableModel.insertRow(0, data);
            stt.close();
            kon.close();
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
            membersihkan_teks();
            simpan.setEnabled(false);
            nonaktif_text();
            simpan.setEnabled(false);
            nonaktif_text();
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(null,
                    ex.getMessage(),"Error",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }
}//GEN-LAST:event_simpanActionPerformed

